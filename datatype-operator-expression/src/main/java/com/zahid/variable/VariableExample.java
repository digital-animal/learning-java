package com.zahid.variable;

public class VariableExample {

	public static void main(String[] args) {
		int num = 10;
		long a = 4923578160L;
		float b = 3.14f;
		double c = 3.1415926535897932384;
		boolean x = true;
		int z = 5;
		
		System.out.println(num);
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(x);
		System.out.println(Math.pow(z,6));
		System.out.println((int) Math.pow(z,6));
	}

}
